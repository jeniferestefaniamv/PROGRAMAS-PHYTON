# Convertir grados celcius en grados farenheit


print("\nConvirtiendo grados Centigrados a Farenheit ")

celcius = float(input("Grados centigrados ?"))
farenheit = (celcius * 9 / 5) + 32
print(f"{celcius} grados centigrados equivalen a {farenheit:.2f} grados farenheit")

print("\nProceso Terminado...")